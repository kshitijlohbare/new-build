import React, { useState } from 'react'
import './App.css'

function App() {
  const [count, setCount] = useState(0)

  return (
    <div className="App">
      <header className="App-header">
        <h1>Standalone React App</h1>
        <p>This is a minimal React app that works independently.</p>
        
        <div className="card">
          <button onClick={() => setCount((count) => count + 1)}>
            Count is {count}
          </button>
          <p>
            Edit <code>src/App.tsx</code> and save to test hot module replacement.
          </p>
        </div>
      </header>
    </div>
  )
}

export default App
